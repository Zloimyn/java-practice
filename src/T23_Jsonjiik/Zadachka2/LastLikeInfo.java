package T23_Jsonjiik.Zadachka2;

public class LastLikeInfo {
    private String user;
    private int hours;
    private int minutes;

    public LastLikeInfo() {}

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public int getHours() {
        return hours;
    }

    public void setHours(int hours) {
        this.hours = hours;
    }

    public int getMinutes() {
        return minutes;
    }

    public void setMinutes(int minutes) {
        this.minutes = minutes;
    }
}
